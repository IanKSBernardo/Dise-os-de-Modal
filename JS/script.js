// 1° MODAL
const modal1 = document.querySelector(".modal-1");
const openButton1 = document.querySelector(".openBTN-1");
const closeButton1 = document.querySelector(".closeBTN-1");

openButton1.addEventListener("click", (e)=>{
    e.preventDefault();
    modal1.showModal();
});

closeButton1.addEventListener("click", (e)=>{
    e.preventDefault
    modal1.close();
});


